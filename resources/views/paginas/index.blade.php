        <p>Após o teste beta, o Club Penguin foi disponibilizado ao público em 24 de outubro de 2005; e se expandiu para uma grande comunidade online, de tal forma que, no final de 2007, o jogo continha mais de 30 milhões de usuários. Em julho de 2013, o Club Penguin atingiu mais de 200 milhões de contas registradas. Embora o jogo fosse gratuito, seu rendimento era predominantemente feito através de assinaturas pagas, o que permitia aos jogadores acessar uma variedade de recursos adicionais, como a capacidade de comprar roupas virtuais, móveis e animais de estimação (chamados puffles) para seus pinguins através do uso da moeda do jogo. O sucesso do Club Penguin levou a New Horizon a ser comprada pela The Walt Disney Company em agosto de 2007, pelo valor de 350 milhões de dólares, com um bônus adicional de 350 milhões de dólares em metas específicas para 2009.</p>
